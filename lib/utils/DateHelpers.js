class DateHelpers {
    static getMonthName(index) {
        const months = [
            'January',
            'February',
            'March',
            'April',
            'May',
            'June',
            'July',
            'August',
            'September',
            'October',
            'November',
            'December',
        ];

        return months[index];
    }
}

export default DateHelpers;
